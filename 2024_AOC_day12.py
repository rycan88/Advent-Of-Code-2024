from collections import defaultdict
L = inp.split("\n")

D = defaultdict(str)
for y in range(len(L)):
    for x in range(len(L[0])):
        coord = (x, y)
        thing = L[y][x]
        D[coord] = thing

def addTups(t1, t2):
    return (t1[0] + t2[0], t1[1] + t2[1])

Found = set()
Around = [(0, 1), (1, 0), (-1, 0), (0, -1)]
total = 0
for y in range(len(L)):
    for x in range(len(L[0])):
        coord = (x, y)
        if coord in Found:
            continue 

        thing = D[coord]
        S = set([coord])
        Current = [coord]

        Perimeter = set()
        while len(Current) > 0:
            current = Current.pop(0)
            for around in Around:
                newCoord = addTups(current, around)
                if newCoord in S:
                    continue
                if (D[newCoord] == thing):
                    Current.append(newCoord)
                    S.add(newCoord)
                else:
                    Perimeter.add((newCoord, around))

        perimeter = 0
        P = set()
        for tup in Perimeter:
            Current = [tup]
            if tup in P:
                continue

            while len(Current) > 0:
                tup = Current.pop(0) 
