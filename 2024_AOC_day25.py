L = inp.split("\n\n")

Locks = []
Keys = []

for thing in L:
    Box = thing.split("\n")
    B = []
    if Box[0][0] == "#":
        for x in range(len(Box[0])):
            for y in range(len(Box)):
                if Box[y][x] == ".":
                    B.append(y)
                    break
        Locks.append(B)
    else:
        for x in range(len(Box[0])):
            for y in range(len(Box) - 1, -1, -1):
                if Box[y][x] == ".":
                    B.append(len(Box) - 1 - y)
                    break
        Keys.append(B)

total = 0
for lock in Locks:
    for key in Keys:
        valid = True
        for i in range(len(key)):
            if key[i] + lock[i] > 7:
                valid = False
                break
        if valid:
            total += 1
print(total)
