L = inp.split("\n")

D = {}
Walls = set()
normalTime = 1
for y in range(len(L)):
    for x in range(len(L[0])):
        coord = (x, y)
        thing = L[y][x]
        
        D[coord] = thing
        if thing == "S":
            startCoord = coord
            D[coord] = "."
        elif thing == "E":
            endCoord = coord
            D[coord] = "."
        elif thing == ".":
            normalTime += 1
        elif thing == "#":
            Walls.add(coord)

Around = [(1, 0), (0, 1), (-1, 0), (0, -1)]
def addTups(t1, t2):
    return (t1[0] + t2[0], t1[1] + t2[1])

def distance(t1, t2):
    return abs(t1[0] - t2[0]) + abs(t1[1] - t2[1])

start = startCoord
end = endCoord

Found = set([start])
Steps = {}
Coords = {}
current = start
Steps[0] = start
Coords[start] = 0
step = 0
while current != end:
    step += 1
    for around in Around:
        newCoord = addTups(current, around)
        if newCoord in D and newCoord not in Found and D[newCoord] == ".":
            Coords[newCoord] = step
            Steps[step] = newCoord
            Found.add(newCoord)
            current = newCoord
            break

timeDiff = 100
cheatSeconds = 20
total = 0
for i in range(normalTime + 1):
    current = Steps[i]
    for j in range(i + timeDiff, normalTime + 1):
        nextCoord = Steps[j]
        d = distance(current, nextCoord)
        if d <= cheatSeconds and i + d - j <= -timeDiff: 
            total += 1

print(total)